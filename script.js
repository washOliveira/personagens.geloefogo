function botaoclicado(event){
	toggleButton(event.target);
	var texto = event.target.textContent;
	filtrar(texto);
}

function toggleButton(element) {
	var pressed = (element.getAttribute("aria-pressed") === "true");
	element.setAttribute("aria-pressed", !pressed);
}

function filtrar(escolhido){
	const filtradoFamilia = personagens.filter(p => p.familia === escolhido);
	const filtradoGrupo = personagens.filter(p => p.grupo === escolhido);

	var ul, li, span, txtValue;
    ul = document.getElementById("myUL");
    li = ul.getElementsByTagName("li");
  
    if(filtradoFamilia.length > 0){
		for (i = 0; i < li.length; i++) {
	    	span = li[i].getElementsByTagName("span")[0];
	        txtValue = span.textContent || span.innerText;

			if(txtValue.indexOf(filtradoFamilia[0].familia) > -1){
	 			li[i].style.display = "";
	        }  else{
	        	li[i].style.display = "none";
	        }
	    }
    }else{
	    for (i = 0; i < li.length; i++) {
			span = li[i].getElementsByTagName("span")[1];
		    txtValue = span.textContent || span.innerText;

		    if(txtValue.indexOf(filtradoGrupo[0].grupo) > -1){
		        li[i].style.display = "";
		    } else{
		    	li[i].style.display = "none";
		    }
		}
    }
}

function consultarPersonagem(){
    var input, filter, ul, li, p, txtValue;
    input = document.getElementById("myInput");
    filter = input.value.toUpperCase();
    ul = document.getElementById("myUL");
    li = ul.getElementsByTagName("li");
    for (i = 0; i < li.length; i++) {
        p = li[i].getElementsByTagName("p")[0];
        txtValue = p.textContent || p.innerText;
        if (txtValue.toUpperCase().indexOf(filter) > -1) {
            li[i].style.display = "";
        } else {
            li[i].style.display = "none";
        }
    }
}

